void test::TestParallaxMapping::OnRender()
{
	glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	m_Shader->Bind();
	m_Proj = m_Camera->GetProjMatrix();
	m_View = m_Camera->GetViewMatrix();
	m_Model = glm::mat4(1.0f);
	m_Model = glm::rotate(m_Model, glm::radians((float)glfwGetTime() * -10.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
	m_Shader->SetUniformsMat4f("projection", m_Proj);
	m_Shader->SetUniformsMat4f("view", m_View);
	m_Shader->SetUniformsMat4f("model", m_Model);
	m_Shader->SetUniforms3f("viewPos", m_Camera->GetPos());
	m_Shader->SetUniforms3f("lightPos", m_LightPos);
	m_Shader->SetUniform1f("heightScale", heightScale);
	GLCall(glActiveTexture(GL_TEXTURE0));
	GLCall(glBindTexture(GL_TEXTURE_2D, m_DiffuseMap));
	GLCall(glActiveTexture(GL_TEXTURE1));
	GLCall(glBindTexture(GL_TEXTURE_2D, m_NormalMap));
	GLCall(glActiveTexture(GL_TEXTURE2));
	GLCall(glBindTexture(GL_TEXTURE_2D, m_HeightMap));
	RenderQuad();

	m_Model = glm::mat4(1.0f);
	m_Model = glm::translate(m_Model, m_LightPos);
	m_Model = glm::scale(m_Model, glm::vec3(0.1f));
	m_Shader->SetUniformsMat4f("model", m_Model);
	RenderQuad();
	m_Shader->Unbind();
}

